public class if_else_else_if {
    public static void main(String[] args) {
        double a=13;
        double b=12;
        if(a<b){
            System.out.println("A is smaller");
        }
        else if(a>b){
            System.out.println("A is smaller");}
        else{
            System.out.println("A is equal");
        }
    }
}
